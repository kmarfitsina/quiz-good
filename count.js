function show_res() {
    let counts = JSON.parse(localStorage.getItem('counters'));
    let username = localStorage.getItem('username');

    if (!counts) {
        document.getElementById('subject').textContent = 'Результат не найден :(';
        document.getElementById('description').textContent = 'Сначала пройдите тест ;)';
        return;
    }

    let best = '';
    let max_pos = -1;

    for (let subject in counts) {
        if (counts[subject] > max_pos) {
            max_pos = counts[subject];
            best = subject;
        }
    }

    if (username) {
        document.getElementById('userGreeting').textContent = username + ', ваш результат:';
    }

    document.getElementById('subject').textContent = subject_names[best];
    document.getElementById('description').textContent = text_res[best];
    document.getElementById('resultImg').src = subject_images[best];
}

function start_test() {
    let input = document.querySelector('.name-input');
    let username = input.value.trim();

    if (!username) {
        alert('Введите имя!');
        return;
    }

    localStorage.removeItem('counters');
    localStorage.setItem('username', username);

    window.location.href = 'page2.html';
}
