function get_sub_for_ans(question_name, next_page) {
    let selected = document.querySelector(`input[name="${question_name}"]:checked`);
    

    if (!selected) {
        alert("Пожалуйста, выберите вариант ответа!");
        return; 
    }

    let subject = all_asw[question_name][selected.value];
    slovar[subject]++;

    localStorage.setItem('counters', JSON.stringify(slovar));
    window.location.href = next_page;
}
